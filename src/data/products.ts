export function calculateSmartScore(product: Product): number {
  const maxPrice = 2000;
  const normalizedPrice = 1 - Math.min(product.price / maxPrice, 1);
  const normalizedRating = (product.rating - 1) / 4;
  const maxReviews = 5000;
  const normalizedReviews = Math.min(product.reviewCount / maxReviews, 1);

  const score = (normalizedPrice * 0.3) + (normalizedRating * 0.5) + (normalizedReviews * 0.2);
  return Math.round(score * 100);
}

export function getWarrantyPrice(product: Product, level: WarrantyLevel): number {
  if (level === 'none') return 0;
  const reliabilityFactor = 1 - (product.mtbf / 100000);
  const categoryMultiplier: Record<Category, number> = {
    laptops: 0.12,
    smartphones: 0.10,
    smarthome: 0.08,
    tablets: 0.09,
    accessories: 0.07,
  };
  const levelMultiplier: Record<WarrantyLevel, number> = {
    none: 0,
    basic: 1,
    proactive: 1.8,
    max: 2.6,
  };
  const base = product.price * categoryMultiplier[product.category] * levelMultiplier[level];
  return Math.round(base * (1 + reliabilityFactor) * 100) / 100;
}
